console.log("======================= Operators =========================");

// Assignment operator

// Assign the value 5 to x
let x = 5;
// Assign the value 2 to y
let y = 2;

// Addition operator
let additionResult = x + y;
console.log("Addition result: ", additionResult);

// Subtraction operator
let subtractionResult = x - y;
console.log("Subtraction result: ", subtractionResult);

// Multiplication operator
let multiplicationResult = x * y;
console.log("Multiplication result: ", multiplicationResult);

// Division operator
let divisionResult = x / y;
console.log("Division result: ", divisionResult);

// exponentiation operator
let exponentiationResult = x ** y;
console.log("Exponentiation result: ",exponentiationResult);

// exponentiation operator
let modulusResult = x % y;
console.log("Modulus result: ", modulusResult);


